# ── Memory profiling ──────────────────────────────────────────────────────────

def _peak_memory_mb(device: str) -> float:
    """Return peak memory usage in MB for the current device."""
    try:
        if device == "cuda":
            mb = torch.cuda.max_memory_allocated() / 1024 ** 2
            torch.cuda.reset_peak_memory_stats()
            return float(mb)
        elif device == "mps":
            return float(torch.mps.current_allocated_memory() / 1024 ** 2)
        else:
            try:
                import psutil
                return float(psutil.Process().memory_info().rss / 1024 ** 2)
            except ImportError:
                return float("nan")
    except Exception:
        return float("nan")


# ── Graph structural metrics ──────────────────────────────────────────────────

def _topology_meta(sparse_data: Data) -> dict:
    """Compute clustering coefficient and algebraic connectivity for a sparse graph."""
    try:
        adj = to_scipy_sparse_matrix(
            sparse_data.edge_index.cpu(), num_nodes=sparse_data.num_nodes
        )
        topo = compute_topology_metrics(adj)
        return {
            "clustering_coeff":       topo.get("clustering_coefficient", float("nan")),
            "algebraic_connectivity": topo.get("algebraic_connectivity",  float("nan")),
            "num_components":         topo.get("num_connected_components", float("nan")),
        }
    except Exception:
        return {
            "clustering_coeff":       float("nan"),
            "algebraic_connectivity": float("nan"),
            "num_components":         float("nan"),
        }


# ── Helper: random sparsification ─────────────────────────────────────────────

def _precompute_random_scores(data):
    ei  = data.edge_index.cpu().numpy()
    src, dst = ei[0], ei[1]
    n   = data.num_nodes
    u   = np.minimum(src, dst)
    v   = np.maximum(src, dst)
    keys = u.astype(np.int64) * (n + 1) + v.astype(np.int64)
    _, inverse_idx    = np.unique(keys, return_inverse=True)
    n_undirected      = int(inverse_idx.max()) + 1
    rng               = np.random.default_rng(RANDOM_SCORE_SEED)
    undirected_scores = rng.random(n_undirected)
    return undirected_scores, inverse_idx


def _random_sparsify(data, undirected_scores, inverse_idx, retention_ratio, device):
    if retention_ratio == 1.0:
        return data.clone()
    n_undirected = len(undirected_scores)
    n_keep       = max(1, int(n_undirected * retention_ratio))
    keep_undir   = np.zeros(n_undirected, dtype=bool)
    keep_undir[np.argsort(undirected_scores)[-n_keep:]] = True
    mask         = keep_undir[inverse_idx]
    sparse       = data.clone()
    sparse.edge_index = data.edge_index[:, mask].to(device)
    return sparse


# ── HP sampling ───────────────────────────────────────────────────────────────

def sample_hp_configs(n: int, seed: int = 42) -> list:
    sampler = LatinHypercube(d=5, seed=seed)
    samples = sampler.random(n=n)
    configs = []
    for u_lr, u_wd, u_do, u_hid, u_lay in samples:
        lr     = 10 ** (np.log10(LR_MIN) + u_lr * (np.log10(LR_MAX) - np.log10(LR_MIN)))
        wd     = WD_MIN + u_wd * (WD_MAX - WD_MIN)
        do     = DO_MIN + u_do * (DO_MAX - DO_MIN)
        hidden = HIDDEN_CHOICES[min(int(u_hid * len(HIDDEN_CHOICES)), len(HIDDEN_CHOICES) - 1)]
        layers = LAYER_CHOICES[min(int(u_lay * len(LAYER_CHOICES)), len(LAYER_CHOICES) - 1)]
        configs.append({
            "lr":              float(round(lr, 7)),
            "weight_decay":    float(round(wd, 6)),
            "dropout":         float(round(do, 4)),
            "hidden_channels": int(hidden),
            "num_layers":      int(layers),
        })
    return configs


# ── Training ──────────────────────────────────────────────────────────────────

def train_one_config(graph, num_features, num_classes, hp, device, seeds,
                     arch: str, edge_weight=None):
    accs, f1s, times, mems = [], [], [], []
    ModelClass = ARCH_REGISTRY[arch]
    for seed in seeds:
        torch.manual_seed(seed)
        np.random.seed(seed)
        model = ModelClass(
            in_channels=num_features,
            hidden_channels=hp["hidden_channels"],
            out_channels=num_classes,
            num_layers=hp["num_layers"],
            dropout=hp["dropout"],
        ).to(device)
        model.reset_parameters()
        optimizer = torch.optim.Adam(
            model.parameters(), lr=hp["lr"], weight_decay=hp["weight_decay"]
        )
        trainer = GNNTrainer(model=model, optimizer=optimizer, device=device)
        t0 = time.time()
        _, history = trainer.train_and_evaluate(
            graph, epochs=EPOCHS, patience=PATIENCE,
            edge_weight=edge_weight,
        )
        accs.append(history["test_metrics"]["accuracy"])
        f1s.append(history["test_metrics"]["macro_f1"])
        times.append(time.time() - t0)
        mems.append(_peak_memory_mb(device))
    return {
        "acc_mean":      float(np.mean(accs)),
        "acc_std":       float(np.std(accs)),
        "f1_mean":       float(np.mean(f1s)),
        "f1_std":        float(np.std(f1s)),
        "train_time_s":  float(np.mean(times)),
        "peak_mem_mb":   float(np.mean(mems)),
    }


# ── Robustness stats ──────────────────────────────────────────────────────────

def compute_robustness_stats(hp_results, best_dense_acc, dense_std):
    accs = np.array([r["acc_mean"] for r in hp_results])
    f1s  = np.array([r["f1_mean"]  for r in hp_results])
    q25, q75    = float(np.percentile(accs, 25)), float(np.percentile(accs, 75))
    best_at_r   = float(accs.max())
    std         = float(np.std(accs))
    rob_gain    = float(dense_std / std) if (dense_std is not None and std > 1e-9) else 1.0
    return {
        "acc_mean":            float(np.mean(accs)),
        "acc_std":             std,
        "acc_iqr":             float(q75 - q25),
        "acc_best":            best_at_r,
        "acc_worst":           float(accs.min()),
        "f1_mean":             float(np.mean(f1s)),
        "f1_std":              float(np.std(f1s)),
        "yield_rate_within":   float((accs >= YIELD_THRESHOLD * best_at_r).mean()),
        "yield_rate_vs_dense": float((accs >= YIELD_THRESHOLD * best_dense_acc).mean()),
        "robustness_gain":     rob_gain,
    }


