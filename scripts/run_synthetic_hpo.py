"""Synthetic HPO Robustness experiment.

Same experiment as run_hpo_robustness.py, but the graph is generated by the
cSBM model (synth-graph-rs) instead of loaded from a real dataset.

This enables controlled sweeps over:
  --h            : edge homophily (the primary IV)
  --mu           : feature signal strength (confounder check)
  --d            : average degree (confounder check)
  --theta_exp    : Pareto degree-heterogeneity exponent (Texas replication)
  --arch         : GNN architecture (gcn / graphsage / gat)

Output: results/hpo/synth_h{h}_mu{mu}_d{d}_c{c}_n{n}_gs{gs}[_arch_{arch}]_hpo_{metric}.json
        arch suffix omitted for gcn (backward-compatible with existing results).
        Same schema as real-dataset HPO results — NB06/NB07 loader works unchanged.

Usage:
    python scripts/run_synthetic_hpo.py --h 0.1 --mu 1.0 --metric jaccard
    python scripts/run_synthetic_hpo.py --h 0.5 --mu 2.0 --metric random --graph_seed 1
    python scripts/run_synthetic_hpo.py --h 0.1 --mu 1.0 --theta_exp 3.0 --metric approx_er
    python scripts/run_synthetic_hpo.py --h 0.5 --metric jaccard --arch graphsage
    python scripts/run_synthetic_hpo.py --h 0.5 --metric jaccard --use_edge_weights
"""

