# ── Sparsification dispatch ───────────────────────────────────────────────────

def _parse_composite_metric(metric: str) -> tuple[str, str]:
    """Return (strategy, base_metric) for composite metric names.

    E.g. 'degree_aware_jaccard' → ('degree_aware', 'jaccard')
         'metric_backbone_approx_er' → ('metric_backbone', 'approx_er')
         'sampled_adamic_adar' → ('sampled', 'adamic_adar')
         'jaccard' → ('threshold', 'jaccard')
         'jaccard_inv' → ('inverse', 'jaccard')
         'random' → ('random', 'random')
    """
    for prefix in ("metric_backbone_", "degree_aware_", "sampled_"):
        if metric.startswith(prefix):
            return prefix.rstrip("_"), metric[len(prefix):]
    if metric.endswith("_inv"):
        return "inverse", metric[:-4]
    if metric == "random":
        return "random", "random"
    return "threshold", metric


def _build_sparsify_fn(metric: str, data: Data, device: str,
                       use_edge_weights: bool):
    """Return (preprocess_time_s, _sparsify_fn, edge_scores_or_None, is_backbone).

    For backbone metrics the returned fn ignores `r` and always returns the
    backbone graph.  Callers should detect `is_backbone=True` and skip the
    standard retention-rate loop, running the backbone separately.
    """
    strategy, base = _parse_composite_metric(metric)

    t0 = time.time()
    if strategy == "random":
        undirected_scores, inverse_idx = _precompute_random_scores(data)
        preprocess_time = time.time() - t0
        scores_for_weights = None

        def _sparsify(r):
            return _random_sparsify(data, undirected_scores, inverse_idx, r, device)

        return preprocess_time, _sparsify, scores_for_weights, False

    # All non-random strategies use GraphSparsifier
    sparsifier = GraphSparsifier(data, device)
    sparsifier.compute_scores(base)
    preprocess_time = time.time() - t0

    # Edge scores for edge_weight (GCN only, when --use_edge_weights)
    scores_for_weights = None
    if use_edge_weights:
        raw = sparsifier.scores.get(base)
        if raw is not None:
            scores_for_weights = torch.tensor(raw, dtype=torch.float, device=device)

    if strategy == "threshold":
        def _sparsify(r):
            return sparsifier.sparsify(base, r)
    elif strategy == "inverse":
        def _sparsify(r):
            return sparsifier.sparsify(base, r, keep_lowest=True)
    elif strategy == "degree_aware":
        def _sparsify(r):
            return sparsifier.sparsify_degree_aware(base, r)
    elif strategy == "sampled":
        def _sparsify(r):
            return sparsifier.sparsify_sampled(base, r)
    elif strategy == "metric_backbone":
        # Backbone ignores r — will be handled separately after the main loop
        def _sparsify(r):
            sparse_data, _ = sparsifier.sparsify_metric_backbone(base)
            return sparse_data
        return preprocess_time, _sparsify, scores_for_weights, True
    else:
        raise ValueError(f"Unknown strategy: {strategy}")

    return preprocess_time, _sparsify, scores_for_weights, False


# ── Main experiment loop ──────────────────────────────────────────────────────

def run_synthetic(
    h: float, mu: float, d: float, n: int, c: int, f: int,
    graph_seed: int, theta_exp: float | None,
    device: str, resume: bool, metric: str,
    arch: str, use_edge_weights: bool,
) -> None:
    ds_name     = dataset_name(h, mu, d, c, n, graph_seed, theta_exp, arch)
    output_path = RESULTS_DIR / f"{ds_name}_hpo_{metric}.json"

    existing_results = {}
    completed_keys   = set()
    if resume and output_path.exists():
        with open(output_path) as fh:
            existing = json.load(fh)
        existing_results = existing.get("results", {})
        completed_keys   = set(existing_results.keys())
        print(f"  Resuming — {len(completed_keys)} keys done: "
              f"{sorted(completed_keys, key=lambda x: (x != 'backbone', float(x) if x != 'backbone' else 0))}")

    print(f"\n{'#'*70}")
    print(f"# SYNTHETIC: {ds_name}  |  Metric: {metric}  |  Arch: {arch}  |  Device: {device}")
    print(f"{'#'*70}")

    print(f"  Generating cSBM graph: h={h}, mu={mu}, d={d}, n={n}, c={c}, "
          f"f={f}, graph_seed={graph_seed}, theta_exp={theta_exp}")
    data, effective_h = build_csbm_graph(h, mu, d, n, c, f, graph_seed, theta_exp, device)
    num_features = f
    num_classes  = c

    print(f"  {data.num_nodes:,} nodes  {data.edge_index.size(1):,} edges  "
          f"requested_h={h:.3f}  effective_h={effective_h:.3f}")

    synth_meta = {
        "requested_h":       h,
        "effective_h_dense": effective_h,
        "mu":                mu,
        "d":                 d,
        "n":                 n,
        "c":                 c,
        "f":                 f,
        "graph_seed":        graph_seed,
        "theta_exponent":    theta_exp,
        "arch":              arch,
    }

    hp_configs = sample_hp_configs(N_HP_SAMPLES, seed=42)
    print(f"  Sampled {N_HP_SAMPLES} HP configs (LHC, seed=42)")

    print(f"  Building sparsifier for metric={metric}...")
    preprocess_time, _sparsify, edge_scores, is_backbone = _build_sparsify_fn(
        metric, data, device, use_edge_weights
    )
    print(f"  Preprocessing done in {preprocess_time:.1f}s")

    results   = existing_results.copy()
    seeds     = list(range(N_SEEDS))
    dense_std = None

    if "1.0" in results:
        dense_std = results["1.0"]["robustness_stats"]["acc_std"]

    # ── Standard retention-rate loop ──────────────────────────────────────────
    # For backbone metrics: only run r=1.0 (dense baseline); backbone handled below
    rates_to_run = [1.0] if is_backbone else RETENTION_RATES

    for r in sorted(rates_to_run, reverse=True):
        r_key = str(r)

        if r_key in completed_keys:
            print(f"  r={r:.1f}  SKIPPED (already done)")
            if r == 1.0 and dense_std is None:
                dense_std = results[r_key]["robustness_stats"]["acc_std"]
            continue

        sparse_data = _sparsify(r)
        n_edges     = int(sparse_data.edge_index.size(1))

        s_src = sparse_data.edge_index[0].cpu()
        s_dst = sparse_data.edge_index[1].cpu()
        y_cpu = data.y.cpu()
        sparse_effective_h = float((y_cpu[s_src] == y_cpu[s_dst]).float().mean().item())

        # Edge weights for this retention ratio (threshold on scores, GCN only)
        ew = None
        if use_edge_weights and edge_scores is not None and arch == "gcn":
            # Build a mask matching the sparsified edge_index
            full_ei   = data.edge_index.cpu()
            sparse_ei = sparse_data.edge_index.cpu()
            full_set  = {(int(full_ei[0, i]), int(full_ei[1, i])): i
                         for i in range(full_ei.size(1))}
            idx       = [full_set[(int(sparse_ei[0, j]), int(sparse_ei[1, j]))]
                         for j in range(sparse_ei.size(1))
                         if (int(sparse_ei[0, j]), int(sparse_ei[1, j])) in full_set]
            ew = edge_scores[idx] if idx else None

        topo = _topology_meta(sparse_data)

        print(f"\n  r={r:.1f}  ({n_edges:,} edges, eff_h={sparse_effective_h:.3f})  —  "
              f"{N_HP_SAMPLES} configs × {N_SEEDS} seeds")

        hp_results = []
        t_start    = time.time()
        for i, hp in enumerate(hp_configs):
            try:
                res = train_one_config(
                    sparse_data, num_features, num_classes, hp, device, seeds,
                    arch=arch, edge_weight=ew,
                )
            except Exception as e:
                print(f"    [SKIP hp_idx={i}] {type(e).__name__}: {e}")
                continue
            hp_results.append({"hp_idx": i, **res})

            if (i + 1) % 10 == 0:
                mean_so_far = np.mean([x["acc_mean"] for x in hp_results])
                elapsed     = (time.time() - t_start) / 60
                print(f"    [{i+1:3d}/{N_HP_SAMPLES}]  "
                      f"running mean acc={mean_so_far:.4f}  "
                      f"elapsed={elapsed:.1f}m")

        best_dense_acc = (
            float(max(x["acc_mean"] for x in hp_results))
            if r == 1.0
            else float(results["1.0"]["meta"]["best_dense_acc"])
        )

        stats = compute_robustness_stats(hp_results, best_dense_acc, dense_std)
        if r == 1.0:
            dense_std = stats["acc_std"]

        results[r_key] = {
            "meta": {
                "retention_ratio":  r,
                "n_edges":          n_edges,
                "best_dense_acc":   best_dense_acc,
                "effective_h":      sparse_effective_h,
                "preprocess_time_s": preprocess_time,
                **topo,
            },
            "hp_results":       hp_results,
            "robustness_stats": stats,
        }

        _checkpoint(output_path, ds_name, metric, num_features, num_classes,
                    hp_configs, results, synth_meta, arch)

        print(f"  r={r:.1f}  DONE  "
              f"std={stats['acc_std']:.4f}  "
              f"yield_within={stats['yield_rate_within']:.1%}  "
              f"gain={stats['robustness_gain']:.2f}x  "
              f"f1={stats['f1_mean']:.4f}  "
              f"[{(time.time()-t_start)/60:.1f}m]")

    # ── Backbone: run once after the loop ─────────────────────────────────────
    if is_backbone and "backbone" not in completed_keys:
        print(f"\n  Running metric backbone (r determined by graph structure)...")
        if dense_std is None and "1.0" in results:
            dense_std = results["1.0"]["robustness_stats"]["acc_std"]

        backbone_data = _sparsify(1.0)  # backbone fn ignores r
        n_edges       = int(backbone_data.edge_index.size(1))
        actual_r      = n_edges / int(data.edge_index.size(1))

        s_src = backbone_data.edge_index[0].cpu()
        s_dst = backbone_data.edge_index[1].cpu()
        y_cpu = data.y.cpu()
        backbone_eff_h = float((y_cpu[s_src] == y_cpu[s_dst]).float().mean().item())
        topo           = _topology_meta(backbone_data)

        best_dense_acc = float(results["1.0"]["meta"]["best_dense_acc"]) if "1.0" in results else 0.0

        print(f"  backbone  ({n_edges:,} edges, actual_r={actual_r:.3f}, "
              f"eff_h={backbone_eff_h:.3f})  —  {N_HP_SAMPLES} configs × {N_SEEDS} seeds")

        hp_results = []
        t_start    = time.time()
        for i, hp in enumerate(hp_configs):
            try:
                res = train_one_config(
                    backbone_data, num_features, num_classes, hp, device, seeds,
                    arch=arch,
                )
            except Exception as e:
                print(f"    [SKIP hp_idx={i}] {type(e).__name__}: {e}")
                continue
            hp_results.append({"hp_idx": i, **res})

            if (i + 1) % 10 == 0:
                elapsed = (time.time() - t_start) / 60
                print(f"    [{i+1:3d}/{N_HP_SAMPLES}]  "
                      f"running mean acc={np.mean([x['acc_mean'] for x in hp_results]):.4f}  "
                      f"elapsed={elapsed:.1f}m")

        stats = compute_robustness_stats(hp_results, best_dense_acc, dense_std)

        results["backbone"] = {
            "meta": {
                "retention_ratio":   actual_r,
                "n_edges":           n_edges,
                "best_dense_acc":    best_dense_acc,
                "effective_h":       backbone_eff_h,
                "preprocess_time_s": preprocess_time,
                **topo,
            },
            "hp_results":       hp_results,
            "robustness_stats": stats,
        }

        _checkpoint(output_path, ds_name, metric, num_features, num_classes,
                    hp_configs, results, synth_meta, arch)

        print(f"  backbone  DONE  "
              f"actual_r={actual_r:.3f}  "
              f"std={stats['acc_std']:.4f}  "
              f"yield_within={stats['yield_rate_within']:.1%}  "
              f"gain={stats['robustness_gain']:.2f}x  "
              f"[{(time.time()-t_start)/60:.1f}m]")

    print(f"\n  Saved → {output_path}")
    del data
    gc.collect()
    if device == "cuda":
        torch.cuda.empty_cache()


# ── CLI ───────────────────────────────────────────────────────────────────────

def get_device() -> str:
    if torch.cuda.is_available():
        return "cuda"
    if torch.backends.mps.is_available():
        return "mps"
    return "cpu"


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Synthetic HPO Robustness: sweep cSBM parameters to isolate "
                    "the causal driver of HP landscape ruggedness."
    )
    parser.add_argument("--h",          type=float, required=True,
                        help="Requested edge homophily ratio [0, 1]")
    parser.add_argument("--mu",         type=float, default=1.0,
                        help="Feature signal strength (default: 1.0)")
    parser.add_argument("--d",          type=float, default=10.0,
                        help="Average degree (default: 10.0)")
    parser.add_argument("--n",          type=int,   default=1000,
                        help="Number of nodes (default: 1000)")
    parser.add_argument("--c",          type=int,   default=5,
                        help="Number of communities/classes (default: 5)")
    parser.add_argument("--f",          type=int,   default=512,
                        help="Feature dimensionality (default: 512)")
    parser.add_argument("--graph_seed", type=int,   default=0,
                        help="RNG seed for graph generation (default: 0)")
    parser.add_argument("--theta_exp",  type=float, default=None,
                        help="Pareto exponent for degree heterogeneity (default: None = uniform)")
    parser.add_argument("--metric",     type=str,   default="jaccard",
                        choices=ALL_METRICS,
                        help="Sparsification metric/strategy")
    parser.add_argument("--arch",       type=str,   default="gcn",
                        choices=list(ARCH_REGISTRY.keys()),
                        help="GNN architecture (default: gcn)")
    parser.add_argument("--use_edge_weights", action="store_true",
                        help="Pass sparsification scores as edge weights (GCN only)")
    parser.add_argument("--resume",     action="store_true")
    args = parser.parse_args()

    if args.use_edge_weights and args.arch != "gcn":
        parser.error("--use_edge_weights is only supported for --arch gcn")

    device = get_device()
    print(f"Device: {device}  |  Metric: {args.metric}  |  Arch: {args.arch}")
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    t0 = time.time()
    run_synthetic(
        h=args.h, mu=args.mu, d=args.d, n=args.n, c=args.c, f=args.f,
        graph_seed=args.graph_seed, theta_exp=args.theta_exp,
        device=device, resume=args.resume, metric=args.metric,
        arch=args.arch, use_edge_weights=args.use_edge_weights,
    )
    print(f"\nCompleted in {(time.time()-t0)/60:.1f} minutes")


if __name__ == "__main__":
    main()
